package day19_Loops;
public class ForLoopIntro {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("------------------------------------------");
        for(int i=0; i<=5; i+=2){
            System.out.println("Hello World"); // or you can prevent infinite prints with "break" method

        }

    }

}
